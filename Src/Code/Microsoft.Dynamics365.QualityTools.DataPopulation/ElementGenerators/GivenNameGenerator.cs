    public class GivenNameGenerator : DataElementGenerator<string, GivenNameGenerationOptions>
    {
        private static readonly WeightedRandomizer<string> _femaleNamesWeightedRandomizer = new WeightedRandomizer<string>();
        private static readonly WeightedRandomizer<string> _maleNamesWeightedRandomizer = new WeightedRandomizer<string>();

        static GivenNameGenerator()
        {
            _femaleNamesWeightedRandomizer.Add(DataElementGenerator.Values.FemaleGivenNames);
            _maleNamesWeightedRandomizer.Add(DataElementGenerator.Values.MaleGivenNames);
        }

        public GivenNameGenerator() 
            : base(GivenNameGenerationOptions.Default)
        {
        }

        public GivenNameGenerator(GivenNameGenerationOptions options)
            : base(options)
        {
        }

        public override string Generate(GivenNameGenerationOptions options)
        {
            Gender preference = options.GenderPreference;

            if (preference == Gender.Any)
            {
                preference = DataPopulation.DataElementGenerator.Randomizer.NextBoolean() ? Gender.Male : Gender.Female;
            }

            if (options.UseWeightedGeneration)
            {
                return (preference == Gender.Male) 
                    ? _maleNamesWeightedRandomizer.Next() 
                    : _femaleNamesWeightedRandomizer.Next();
            }
            else
            {
                return (preference == Gender.Male)
                    ? DataElementGenerator.Values.MaleGivenNames.RandomKey()
                    : DataElementGenerator.Values.FemaleGivenNames.RandomKey();
            }
        }
    }

    public class GivenNameGenerationOptions : DataElementGenerationOptions
    {
        public Gender GenderPreference { get; set; }
        public bool UseWeightedGeneration { get; set; }

        public static GivenNameGenerationOptions Default => new GivenNameGenerationOptions 
        { 
            GenderPreference = Gender.Any, 
            UseWeightedGeneration = true 
        };
    }
	
